# define
class Model(nn.Module):

    # initialization
    def __init__(self,data_num_channels,data_num_classes,model_stage_1_blocks,model_stage_2_blocks,model_stage_3_blocks,model_stage_4_blocks,model_stage_1_channels,model_stage_2_channels,model_stage_3_channels,model_stage_4_channels,group_width):

        # parent initialization
        super(Model, self).__init__()

        #STEM
        self.stem = nn.ModuleList()
        self.stem.append(nn.Conv2d(data_num_channels, model_stage_1_channels, (3, 3), stride=(1, 1), padding=(1, 1), dilation=(1, 1), groups=1, bias=False, padding_mode='zeros'))
        self.stem.append(nn.BatchNorm2d(model_stage_1_channels, eps=1e-05, momentum=0.1, affine=True, track_running_stats=True))
        self.stem.append(nn.ReLU())

        #STAGE1
        self.stage_1 = nn.ModuleList()
        xblock_s1obj = XBlock(model_stage_1_channels,model_stage_1_channels,group_width,True)
        self.stage_1.append(xblock_s1obj)

        #STAGE2
        self.stage_2 = nn.ModuleList()
        xblock_s2obj = XBlock(model_stage_1_channels,model_stage_2_channels,group_width,False)
        self.stage_2.append(xblock_s2obj)


        #STAGE3
        self.stage_3 = nn.ModuleList()
        for i in range(0,model_stage_3_blocks):
          if i==0:
            xblock_s3obj = XBlock(model_stage_2_channels,model_stage_3_channels,group_width,False)
          else:
            xblock_s3obj = XBlock(model_stage_3_channels,model_stage_3_channels,group_width,True)
          self.stage_3.append(xblock_s3obj)


        #STAGE4
        self.stage_4 = nn.ModuleList()
        for i in range(0,model_stage_4_blocks):
          if i==0:
            xblock_s4obj = XBlock(model_stage_3_channels,model_stage_4_channels,group_width,False)
          else:
            xblock_s4obj = XBlock(model_stage_4_channels,model_stage_4_channels,group_width,True)
          self.stage_4.append(xblock_s4obj)


        #HEAD
        self.head = nn.ModuleList()
        self.head.append(nn.AdaptiveAvgPool2d((1, 1)))
        self.head.append(nn.Flatten())
        self.head.append(nn.Linear(model_stage_4_channels, data_num_classes, bias=True))


        

      
      

    # forward path
    def forward(self, x):

        #STEM
        for layer in self.stem:
          x = layer(x)

        #STAGE 1
        for layer in self.stage_1:
          x = layer(x)
        

        #STAGE 2
        for layer in self.stage_2:
          x = layer(x)

        #STAGE 3
        for layer in self.stage_3:
          x = layer(x)

        #STAGE 4
        for layer in self.stage_4:
          x = layer(x)

        #HEAD
        for layer in self.head:
          x = layer(x)


        return x

