# X block
class XBlock(nn.Module):

    # initialization
    def __init__(self, Ni, No, G, Type):

        # parent initialization
        super(XBlock, self).__init__()

        #Adjust Stride for different XBlock
        self.Type = Type
        if Type:
          s = 1
        else:
          s = 2

        #1 X 1/1 
        self.xblock = nn.ModuleList()
        self.xblock.append(nn.Conv2d(Ni, No, (1, 1), stride=(1, 1), padding=(0, 0), dilation=(1, 1), groups=1, bias=False, padding_mode='zeros'))
        self.xblock.append(nn.BatchNorm2d(No, eps=1e-05, momentum=0.1, affine=True, track_running_stats=True))
        self.xblock.append(nn.ReLU())

        #3 X 3/s 
        self.xblock.append(nn.Conv2d(No, No, (3, 3), stride=(s, s), padding=(1, 1), dilation=(1, 1), groups=8, bias=False, padding_mode='zeros'))
        self.xblock.append(nn.BatchNorm2d(No, eps=1e-05, momentum=0.1, affine=True, track_running_stats=True))
        self.xblock.append(nn.ReLU())

        #1 X 1/1
        self.xblock.append(nn.Conv2d(No, No, (1, 1), stride=(1, 1), padding=(0, 0), dilation=(1, 1), groups=1, bias=False, padding_mode='zeros'))
        self.xblock.append(nn.BatchNorm2d(No, eps=1e-05, momentum=0.1, affine=True, track_running_stats=True))
        self.xblock.append(nn.ReLU())

        #1 X 1/2
        self.stride2_block = nn.ModuleList()
        self.stride2_block.append(nn.Conv2d(Ni, No, (1, 1), stride=(2, 2), padding=(0, 0), dilation=(1, 1), groups=1, bias=False, padding_mode='zeros'))
        self.stride2_block.append(nn.BatchNorm2d(No, eps=1e-05, momentum=0.1, affine=True, track_running_stats=True))
        self.stride2_block.append(nn.ReLU())

        



    # forward path
    def forward(self, x):

        y = x
        for layer in self.xblock:
          x = layer(x)
        if self.Type == False:
          for layer in self.stride2_block:
            y = layer(y)
        x = y + x    

        return x

